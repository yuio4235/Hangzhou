package com.as.order.dao;

public class ChimaFenxiDAO {

	private String chima;
	private String chimazu;
	private int wareAll;
	private int wareCnt;
	private int amount;
	private int price;
	public String getChimazu() {
		return chimazu;
	}
	public void setChimazu(String chimazu) {
		this.chimazu = chimazu;
	}
	public String getChima() {
		return chima;
	}
	public void setChima(String chima) {
		this.chima = chima;
	}
	public int getWareAll() {
		return wareAll;
	}
	public void setWareAll(int wareAll) {
		this.wareAll = wareAll;
	}
	public int getWareCnt() {
		return wareCnt;
	}
	public void setWareCnt(int wareCnt) {
		this.wareCnt = wareCnt;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
}
