package com.as.order.dao;

public class ZhutiFenxiDAO {
	private String zhuti;
	private int wareCnt;
	private int wareAll;
	private int price;
	private int amount;
	public String getZhuti() {
		return zhuti;
	}
	public void setZhuti(String zhuti) {
		this.zhuti = zhuti;
	}
	public int getWareCnt() {
		return wareCnt;
	}
	public void setWareCnt(int wareCnt) {
		this.wareCnt = wareCnt;
	}
	public int getWareAll() {
		return wareAll;
	}
	public void setWareAll(int wareAll) {
		this.wareAll = wareAll;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
}
