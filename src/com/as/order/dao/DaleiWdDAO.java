package com.as.order.dao;

public class DaleiWdDAO {
	private String waretypeid;
	private String dalei;
	private int yd;
	private int wd;
	private int total;
	public String getWaretypeid() {
		return waretypeid;
	}
	public void setWaretypeid(String waretypeid) {
		this.waretypeid = waretypeid;
	}
	public String getDalei() {
		return dalei;
	}
	public void setDalei(String dalei) {
		this.dalei = dalei;
	}
	public int getYd() {
		return yd;
	}
	public void setYd(int yd) {
		this.yd = yd;
	}
	public int getWd() {
		return wd;
	}
	public void setWd(int wd) {
		this.wd = wd;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
}
