package com.as.order.dao;

public class XiaoleiFenxiDAO {

	private String xiaolei;
	private int wareAll;
	private int wareCnt;
	private int price;
	private int amount;
	public String getXiaolei() {
		return xiaolei;
	}
	public void setXiaolei(String xiaolei) {
		this.xiaolei = xiaolei;
	}
	public int getWareAll() {
		return wareAll;
	}
	public void setWareAll(int wareAll) {
		this.wareAll = wareAll;
	}
	public int getWareCnt() {
		return wareCnt;
	}
	public void setWareCnt(int wareCnt) {
		this.wareCnt = wareCnt;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
}
