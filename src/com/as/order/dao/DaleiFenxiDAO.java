package com.as.order.dao;

public class DaleiFenxiDAO {

	private String dalei;
	private int wareAll;
	private int wareCnt;
	private int price;
	private int amount;
	public String getDalei() {
		return dalei;
	}
	public void setDalei(String dalei) {
		this.dalei = dalei;
	}
	public int getWareAll() {
		return wareAll;
	}
	public void setWareAll(int wareAll) {
		this.wareAll = wareAll;
	}
	public int getWareCnt() {
		return wareCnt;
	}
	public void setWareCnt(int wareCnt) {
		this.wareCnt = wareCnt;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
}
