package com.as.order.dao;

public class ZhutiWdDAO {
	private String zhuti;
	private int yd;
	private int wd;
	private int total;
	public String getZhuti() {
		return zhuti;
	}
	public void setZhuti(String zhuti) {
		this.zhuti = zhuti;
	}
	public int getYd() {
		return yd;
	}
	public void setYd(int yd) {
		this.yd = yd;
	}
	public int getWd() {
		return wd;
	}
	public void setWd(int wd) {
		this.wd = wd;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
}
