package com.as.order.dao;

public class SxzWdDAO {
	private String sxz;
	private int yd;
	private int wd;
	private int total;
	public String getSxz() {
		return sxz;
	}
	public void setSxz(String sxz) {
		this.sxz = sxz;
	}
	public int getYd() {
		return yd;
	}
	public void setYd(int yd) {
		this.yd = yd;
	}
	public int getWd() {
		return wd;
	}
	public void setWd(int wd) {
		this.wd = wd;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
}
